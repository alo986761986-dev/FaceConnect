// Get translation for a key
export const getTranslation = (lang, key) => {
  // Check if language exists, fallback to English
  const langTranslations = translations[lang] || translations.en;
  
  // Return translation or fallback to English, or key itself
  return langTranslations[key] || translations.en[key] || key;
};

// Get all translations for a language
export const getTranslations = (lang) => {
  return translations[lang] || translations.en;
};

// Check if language is RTL
export const isRTL = (lang) => {
  const language = LANGUAGES.find(l => l.code === lang);
  return language?.rtl || false;
};

// Get language info
export const getLanguageInfo = (code) => {
  return LANGUAGES.find(l => l.code === code) || LANGUAGES[0];
};

// Create translation hook helper
export const createTranslator = (lang) => {
  return (key) => getTranslation(lang, key);
};

export default translations;
